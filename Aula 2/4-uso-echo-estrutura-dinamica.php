<?php
// Este exemplo demonstra como o PHP pode montar uma estrutura HTML completa
// utilizando lógica e múltiplos comandos echo.

// Definição de variáveis de conteúdo
$titulo = "Área Administrativa";
$descricao = "Painel gerado dinamicamente pelo servidor.";

// Geração do HTML de forma dinâmica
echo "<section>";
echo "<h1>$titulo</h1>";
echo "<p>$descricao</p>";
echo "</section>";
?>
