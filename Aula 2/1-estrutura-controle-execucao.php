<?php
// As tags PHP delimitam o código que será interpretado no servidor.
// Qualquer conteúdo fora dessas tags é tratado como HTML.

// Definição de uma variável para armazenar uma mensagem.
// Variáveis em PHP iniciam com o símbolo $ e possuem tipagem dinâmica.
$mensagem = "Script PHP executado com sucesso no servidor.";

// O comando echo envia o conteúdo processado para o navegador.
// O navegador recebe apenas o resultado final, não o código PHP.
echo "<p>$mensagem</p>";
?>
